const sessionTabs = document.getElementById("session-tabs");
const studentInfo = document.getElementById("student-info");
sessions.forEach((session, index) => {
    const li = document.createElement("li");
    const button = document.createElement("button");
    button.textContent = session.session;
    button.onclick = () => loadStudents(index);
    if (index === 0) button.classList.add("active");
    li.appendChild(button);
    sessionTabs.appendChild(li);
});

function loadStudents(sessionIndex) {
    studentInfo.innerHTML = `
        <h2>${sessions[sessionIndex].session}</h2>
        <table>
            <tr>
                <th>Photo</th>
                <th>Name</th>
                <th>Roll</th>
                <th>Reg</th>
            </tr>
            ${sessions[sessionIndex].students.map(student => `
                <tr>
                    <td><img src="${student.photo}" alt="Student Photo"></td>
                    <td>${student.name}</td>
                    <td>${student.roll}</td>
                    <td>${student.reg}</td>
                </tr>
            `).join('')}
        </table>
    `;

    // Update active tab
    const buttons = document.querySelectorAll("#session-tabs li button");
    buttons.forEach(btn => btn.classList.remove("active"));
    buttons[sessionIndex].classList.add("active");
}

